import { ctx, tileSize } from "../main.js";

// . = empty
// # = path

const level = [
    "...#....................",
    "...#....................",
    "...#....................",
    "...#...##############...",
    "...#...#............#...",
    "...#...#............#...",
    "...#...#............#...",
    "...#...#....#########...",
    "...#...#....#...........",
    "...#...#....#...........",
    "...#...#....#...........",
    "...#####....#...........",
    "............#...########",
    "............#...#.......",
    "............#...#.......",
    "............#...#.......",
    "............#####.......",
    "........................",
    "........................",
    "........................",
]

export function renderLevel() {
    for (let y = 0; y < level.length; y++) {
        const row = level[y];
        for (let x = 0; x < row.length; x++) {
            const tile = row[x];
            if (tile == "#") {
                ctx.fillStyle = "white";
                ctx.fillRect(x * tileSize, y * tileSize, tileSize, tileSize);
            }
        }
    }
}